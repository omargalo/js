// Control de flujo switch

let y=3

switch (y) {
    case 1: {
        console.log('Caso 1');
        break;
    }
    case 2: {
        console.log('Caso 2');
        break;
    }
    case 3: {
        console.log('Caso 3');
        break;
    }
    default: {
        console.log('Caso Default');
        break;
    }
}

